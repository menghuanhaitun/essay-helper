export async function onRequest(context) {
  const { env } = context;

  try {
    // 删除旧数据（如果存在）
    await env.DB.prepare('DELETE FROM vocab_library').all();

    // 导入词汇
    let wordCount = 0;
    for (const [category, words] of Object.entries(VOCAB_DATA.words)) {
      for (const content of words) {
        await env.DB.prepare(
          'INSERT INTO vocab_library (type, category, content) VALUES (?, ?, ?)'
        ).bind('word', category, content).run();
        wordCount++;
      }
    }

    // 导入句子
    let sentenceCount = 0;
    for (const [category, sentences] of Object.entries(VOCAB_DATA.sentences)) {
      for (const content of sentences) {
        await env.DB.prepare(
          'INSERT INTO vocab_library (type, category, content) VALUES (?, ?, ?)'
        ).bind('sentence', category, content).run();
        sentenceCount++;
      }
    }

    return json({
      success: true,
      message: '词库导入成功！',
      stats: {
        words: wordCount,
        sentences: sentenceCount,
        total: wordCount + sentenceCount
      }
    });

  } catch (error) {
    console.error('导入词库失败:', error);
    return json({
      success: false,
      error: error.message
    }, { status: 500 });
  }
}
